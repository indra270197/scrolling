public class Hitech {

    private String id;
    private String nama;
    private String harga;
    private String deskripsi;
    private String lokasi;
    private String lantai;

    public void setHtId (String id)
    {
        this.id = id;
    }

    public String getHtId()
    {
        return id;
    }

    public void setHtName (String nama)
    {
        this.nama = nama;
    }

    public String getHtName()
    {
        return nama;
    }

    public void setHtHarga (String harga)
    {
        this.harga = harga;
    }

    public String getHtHarga()
    {
        return harga;
    }

    public void setHtDeskripsi (String deskripsi)
    {
        this.deskripsi = deskripsi;
    }

    public String getHtDeskripsi()
    {
        return deskripsi;
    }

    public void setHtLokasi (String lokasi)
    {
        this.lokasi = lokasi;
    }

    public String getHtLokasi()
    {
        return lokasi;
    }

    public void setHtLantai (String lantai)
    {
        this.lantai = lantai;
    }

    public String getHtLantai()
    {
        return lantai;
    }
}